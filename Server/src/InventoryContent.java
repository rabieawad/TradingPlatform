package org.boes.praktikum;

public class InventoryContent {
    int steel = 0;
    int kidny = 0;
    int wood = 0;
    int gold = 0;
    int silver = 0;
    int  dimond = 0;

    public void setSteel(int steel) {
        this.steel = steel;
    }

    public void setKidny(int kidny) {
        this.kidny = kidny;
    }

    public void setWood(int wood) {
        this.wood = wood;
    }

    public void setGold(int gold) {
        this.gold = gold;
    }

    public void setSilver(int silver) {
        this.silver = silver;
    }

    public void setDimond(int dimond) {
        this.dimond = dimond;
    }

    public int getSteel() {
        return steel;
    }

    public int getKidny() {
        return kidny;
    }

    public int getWood() {
        return wood;
    }

    public int getGold() {
        return gold;
    }

    public int getSilver() {
        return silver;
    }

    public int getDimond() {
        return dimond;
    }
}
